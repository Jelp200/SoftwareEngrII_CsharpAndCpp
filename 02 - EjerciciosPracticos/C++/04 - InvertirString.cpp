#include <iostream>
#include <string>

/* --------------------------------------------------------------------------------------------
------------------------------------- N A M E  S P A C E S ------------------------------------
-------------------------------------------------------------------------------------------- */
using namespace std;

/* --------------------------------------------------------------------------------------------
------------------------------- F U N C I O N  P R I N C I P A L ------------------------------
-------------------------------------------------------------------------------------------- */
int main() {
    string texto;
    string invertido = "";

    cout << "Ingrese una cadena de texto: ";
    getline(cin, texto);

    for (int i = texto.size() - 1; i >= 0; i--) {
        invertido += texto[i];
    }

    cout << "Cadena invertida: " << invertido << endl;
    return 0;
}