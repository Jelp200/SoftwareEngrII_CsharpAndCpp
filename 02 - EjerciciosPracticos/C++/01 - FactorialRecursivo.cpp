#include <iostream>

/* --------------------------------------------------------------------------------------------
------------------------------------- N A M E  S P A C E S ------------------------------------
-------------------------------------------------------------------------------------------- */
using namespace std;

/* --------------------------------------------------------------------------------------------
-------------------------- P R O T O T I P O  D E  F U N C I O N E S --------------------------
-------------------------------------------------------------------------------------------- */
int factorial(int n);

/* --------------------------------------------------------------------------------------------
------------------------------- F U N C I O N  P R I N C I P A L ------------------------------
-------------------------------------------------------------------------------------------- */
int main() {
    int n = 0;
    cout << "De un numero n para calcular su factorial (n!): "; cin >> n;
    cout << "El factorial de " << n << " es: " << factorial(n) << endl;
    return 0;
}

/* --------------------------------------------------------------------------------------------
-------------------------------------- F U N C I O N E S --------------------------------------
-------------------------------------------------------------------------------------------- */
int factorial(int n) {
    return (n == 0) ? 1 : n * factorial(n - 1);
}