/* --------------------------------------------------------------------------------------------
--------------------------------------- I N C L U D E S ---------------------------------------
-------------------------------------------------------------------------------------------- */
#include <iostream>

/* --------------------------------------------------------------------------------------------
------------------------------------- N A M E  S P A C E S ------------------------------------
-------------------------------------------------------------------------------------------- */
using namespace std;

/* --------------------------------------------------------------------------------------------
------------------------------------------ C L A S E S ----------------------------------------
-------------------------------------------------------------------------------------------- */
class Animal {
public:
    virtual void hablar() = 0;
};

class Perro : public Animal {
public:
    void hablar() override { cout << "Guau!" << endl; }
};

class Gato : public Animal {
public:
    void hablar() override { cout << "Miau!" << endl; }
};
/* --------------------------------------------------------------------------------------------
------------------------------- F U N C I O N  P R I N C I P A L ------------------------------
-------------------------------------------------------------------------------------------- */
int main() {
    Animal* a1 = new Perro();
    Animal* a2 = new Gato();
    a1->hablar(); // Guau!
    a2->hablar(); // Miau!
    delete a1;
    delete a2;
}