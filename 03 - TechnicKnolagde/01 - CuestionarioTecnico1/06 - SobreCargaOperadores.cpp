/* --------------------------------------------------------------------------------------------
--------------------------------------- I N C L U D E S ---------------------------------------
-------------------------------------------------------------------------------------------- */
#include <iostream>


/* --------------------------------------------------------------------------------------------
------------------------------------------ C L A S E S ----------------------------------------
-------------------------------------------------------------------------------------------- */
class Vector2D {
public:
    int x, y;
    
    Vector2D(int a = 0, int b = 0) : x(a), y(b) {}
    
    Vector2D operator+(const Vector2D& otro) const {
        return Vector2D(x + otro.x, y + otro.y);
    }
};

/* --------------------------------------------------------------------------------------------
------------------------------- F U N C I O N  P R I N C I P A L ------------------------------
-------------------------------------------------------------------------------------------- */
int main() {
    Vector2D v1(2, 3), v2(4, 5);
    Vector2D v3 = v1 + v2;

    std::cout << "Resultado: (" << v3.x << ", " << v3.y << ")" << std::endl;
    return 0;
}