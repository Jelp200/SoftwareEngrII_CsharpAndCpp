#include <iostream>

/* --------------------------------------------------------------------------------------------
------------------------------- F U N C I O N  P R I N C I P A L ------------------------------
-------------------------------------------------------------------------------------------- */
int main() {
    int valor = 13;
    int* ptr = &valor;

    std::cout << "Valor: " << valor << std::endl;
    std::cout << "Dirección: " << ptr << std::endl;
    std::cout << "Valor a través del puntero: " << *ptr << std::endl;
    
    return 0;
}